package basics;

public class CompareTwoCharacters {
	public static void main(String args[]) {
		char ch1 = 'a';
		char ch2 = 'b';
		System.out.println("ch1>ch2 : " + (ch1>ch2));
		System.out.println("ch1<ch2 : " + (ch1<ch2));
		System.out.println("ch1>=ch2 : " + (ch1>=ch2));
		System.out.println("ch1<=ch2 : " + (ch1<=ch2));
		System.out.println("ch1==ch2 : " + (ch1==ch2));
		System.out.println("ch1!=ch2 : " + (ch1!=ch2));
	}
}
